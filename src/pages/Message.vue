<template>
  <div>
    <ul>
      <li v-for="m in messagelist" :key="m.id">
        <!-- 跳转路由并携带query参数 to的字符串写法 -->
        <!-- <router-link :to="`/home/message/detail/${m.id}/${m.title}`">{{m.title}}</router-link>&nbsp;&nbsp;</li> -->
        
        <!-- 跳转路由并携带params参数 to的字符串写法 -->
        <!-- <router-link :to="`/home/message/detail?id=${m.id}&title=${m.title}`">{{m.title}}</router-link>&nbsp;&nbsp;</li> -->
    

         <!-- 跳转路由并携带query参数 to的对象写法 -->
        <router-link :to="{
          path:'/home/message/detail',
          query:{
            id:m.id,
            title:m.title
          }
        }">
        {{m.title}}
        </router-link>
        <button @click="pushShow(m)">push查看</button>
        <button @click="replaceShow(m)">replace查看</button>

        &nbsp;&nbsp;</li>
    </ul>
    <router-view></router-view>
  </div>
</template>

<script>
export default {
  name: "Message",
  data(){
    return {
      messagelist:[
       {id:'001', title :'消息001'},
       {id:'002', title :'消息002'},
       {id:'003', title :'消息003'}
      ]  
    }
  },
  methods: {
    pushShow(m){
      this.$router.push({
          query:{
            id:m.id,
            title:m.title
          }
      })
    },
    replaceShow(m){
      this.$router.replace({
          query:{
            id:m.id,
            title:m.title
          }
      }) 
    }
  },

};
</script>

